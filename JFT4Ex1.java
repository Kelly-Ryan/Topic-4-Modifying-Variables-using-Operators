public class JFT4Ex1{

	public static void main (String[] args) {
		
		int i = 25;
		int j = 6;
		
		System.out.println("Adding two numbers "+(i+j)); 						//25+6=31 
		System.out.println("Subtracting two numbers "+(i-j));					//25-6=19
		System.out.println("Multiplying two numbers "+(i*j));					//25*6=150
		System.out.println("Divding two numbers "+(i/j));						//25/6=4
		System.out.println("Returning the remainder of two numbers "+(i%j));	//remainder of 25/6=1
		
	}

}

